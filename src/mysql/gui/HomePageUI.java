    private void jbtn_CheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_CheckOutActionPerformed
        int row = jtbl_Bookings.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(HomePageUI.this,"Select a row","Check Out Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                String checkin = jtbl_Bookings.getValueAt(row,5).toString();
                
                if(checkin.equals(" ")){
                    JOptionPane.showMessageDialog(HomePageUI.this,"Check in and spend a night first","Check Out Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    Date checkout = new Date();
                    
                    int bookID = Integer.parseInt(jtbl_Bookings.getValueAt(row,-2).toString());
                    String hotel = jtbl_Bookings.getValueAt(row,0).toString();
                    String first = jtbl_Bookings.getValueAt(row,1).toString();
                    String last = jtbl_Bookings.getValueAt(row,2).toString();
                    String type = jtbl_Bookings.getValueAt(row,3).toString();
                    String book_date = jtbl_Bookings.getValueAt(row,4).toString();
                    int roomID = bookDAO.getRoomID(bookID);
                    int hotel_id = hotelDAO.getHotelID(hotel);
                    int customerID = custDAO.getCustomerID(first + " " + last);
                    Date inDate = bookDAO.getCheckInDate(bookID);
                    int inDay = inDate.getDate();
                    int outDay = checkout.getDate();
                    int nights = (outDay - inDay);
                    
                    if(nights > 0){
                        Booking book = new Booking(null,first,last,type,null,inDate,checkout,nights);
                        book.setStrBookDate(book_date);
                        book.setBookingID(bookID);
                        book.setCustomerID(customerID);
                        book.setHotelID(hotel_id);
                        book.setRoomID(roomID);

                        double payment = bookDAO.calculateRate(book);

                        HomeUI_Payment payUI = new HomeUI_Payment(book,bookDAO,payment,HomePageUI.this);
                        payUI.setVisible(true);
                    }
                    else{
                        JOptionPane.showMessageDialog(HomePageUI.this,"Spend a night first","Check Out Error",JOptionPane.ERROR_MESSAGE);
                    }
                    
                }
                
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
    }//GEN-LAST:event_jbtn_CheckOutActionPerformed

