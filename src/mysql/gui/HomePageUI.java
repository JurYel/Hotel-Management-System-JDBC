    private void jtxt_SearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_SearchKeyTyped
        String search = jtxt_Search.getText().trim();
        try{
            int id = hotelDAO.getHotelID(hotel_name);
            if(search.isEmpty()){
                ArrayList<Booking> list = bookDAO.getHotelBookings(id);
                BookingTableModel model = new BookingTableModel(list);
                jtbl_Bookings.setModel(model);
            }
            else{
                ArrayList<Booking> list = bookDAO.searchHotelBooking(id,search);
                BookingTableModel model = new BookingTableModel(list);
                jtbl_Bookings.setModel(model);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jtxt_SearchKeyTyped

    private void jbtn_CancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_CancelActionPerformed
        int row = jtbl_Bookings.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(HomePageUI.this,"Select a row","Cancel Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                Booking book = (Booking)jtbl_Bookings.getValueAt(row,BookingTableModel.OBJECT_COL);
                Date book_date = book.getBookDate();

                /* Alternative class fetching */
                ArrayList<Booking> list = bookDAO.getAllBookings();
                Booking theBook = list.get(row);
                /* END */

                int bookID = Integer.parseInt(jtbl_Bookings.getValueAt(row,-2).toString());
                if(!(bookDAO.checkIfBookingCheckedIn(bookID))){
                    if((JOptionPane.showConfirmDialog(HomePageUI.this,"Cancel booking?","Confirm Cancel",
                            JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE)) == JOptionPane.YES_OPTION){
                        bookDAO.cancelBooking(bookID);
                        populateTable();
                        JOptionPane.showMessageDialog(HomePageUI.this,"Booking has been cancelled!","Cancelled Booking",JOptionPane.INFORMATION_MESSAGE);

                    }
                }
                
            }
            catch(Exception exc){
                exc.printStackTrace();
            } 
        }
       
    }//GEN-LAST:event_jbtn_CancelActionPerformed

