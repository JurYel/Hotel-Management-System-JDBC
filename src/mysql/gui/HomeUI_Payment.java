    private void jbtn_PayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_PayActionPerformed
        try{
            String hotel = jComboBox_Hotel.getSelectedItem().toString();
            String first = booking.getCustomerFirst();
            String last = booking.getCustomerLast();
            int card_num = Integer.parseInt(jtxt_CardNumber.getText());
            double amt = Double.parseDouble(jtxt_Amount.getText());
            Date payDate = new Date();
            String paytype = jComboBox_PayType.getSelectedItem().toString();
            String room_type = jlbl_RoomType.getText();
            int nights = Integer.parseInt(jlbl_Nights.getText());
            
            Payment paymentCheck = new Payment(hotel,first,last,nights,card_num,amt,payDate,paytype);
            paymentCheck.setBookingID(booking.getBookingID());
            paymentCheck.setCustomerID(booking.getCustomerID());
            
            payDAO.addPayment(paymentCheck);
            bookDAO.checkOutRoom(booking);
            homeUI.refreshTable();
            setVisible(false);
            JOptionPane.showMessageDialog(homeUI,"Thank you! Come again!","Transaction Complete",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_PayActionPerformed

