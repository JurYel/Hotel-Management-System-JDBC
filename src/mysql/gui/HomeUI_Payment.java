    private void populateGUI(){
        jlbl_Name.setText(booking.getCustomerFirst() + " " + booking.getCustomerLast());
        jlbl_RoomType.setText(booking.getRoomType());
        jlbl_BookDate.setText(booking.getStrBookDate());
        jlbl_CheckIn.setText(booking.getLSCheckInDate());
        jlbl_CheckOut.setText(booking.getLSCheckOutDate());
        jlbl_Nights.setText(String.format("%d %s",booking.getNumberNights(), (booking.getNumberNights() == 1)? " Night":" Nights"));
        jlbl_Payment.setText(String.valueOf("P"+payment));
        
        try{
            ArrayList<Hotel> list = hotelDAO.getAllHotels();
            
            for(int i = 0;i<list.size();i++){
                jComboBox_Hotel.addItem(list.get(i).getHotelName());
            }
            
            jComboBox_Hotel.setSelectedItem(booking.getHotelName());
            jtxt_Customer.setText(booking.getCustomerFirst() + " " + booking.getCustomerLast());
            jtxt_Amount.setText(jlbl_Payment.getText());
            Date payDate = new Date();
            jtxt_PayDate.setText(payDate.toLocaleString());
            
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
        
    }
